# Tipo de dato entero o largo
numero_entero = 12
print(type(numero_entero), numero_entero)
print(" ")

# Tipo de dato flotante o reales

numero_flotante = 15.5
print(type(numero_flotante),numero_flotante)
print("")

#Tipo de dato Numero complejo
numero_complejo = 5 + 6j
print(type(numero_complejo),numero_complejo)
print(" ")

#Tipo de dato String
nombre = "Milton"
print(type(nombre), nombre)
print(" ")

#Tipo de dato booleano
verdadero_falso = 3 == 3
falso_verdadero = 3 != 3
print(type(verdadero_falso), verdadero_falso , falso_verdadero)
